import UIKit

struct Theme {
    struct Colors {
        static let primary = UIColor(hex: "#000080")
        static let secondary = UIColor(hex: "#33FF57")
        static let greyBackground = UIColor(hex: "#f1f1f1")
         
    }

    struct Fonts {
        static let headline = UIFont.systemFont(ofSize: 32, weight: .bold)
        static let subHeadline = UIFont.systemFont(ofSize: 24, weight: .semibold)
        static let body = UIFont.systemFont(ofSize: 18, weight: .regular)
        static let caption = UIFont.systemFont(ofSize: 14, weight: .light)
         
    }
}

